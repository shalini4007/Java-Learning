package java_Concepts;

public class Day3_SwappingNumbers {

	public static void main(String[] args) {
	int a=10, b=30;
	System.out.println(a);
	System.out.println(b);
	
	int c=a;
	System.out.println(c);
	a=b;
	System.out.println(a);
	System.out.println(b);
	b=c;
	System.out.println(a);
	System.out.println(b);
	}

}
